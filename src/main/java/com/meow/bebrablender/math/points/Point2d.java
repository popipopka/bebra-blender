package com.meow.bebrablender.math.points;

public class Point2d {
    private double x;
    private double y;
    private double depth;

    public Point2d(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getDepth() {
        return depth;
    }
}
